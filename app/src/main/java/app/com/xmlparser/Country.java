package app.com.xmlparser;

class Country {

    private String name, capital, id;


    String getCapital() {
        return capital;
    }

    void setCapital(String capital) {
        this.capital = capital;
    }

    String getId() {
        return id;
    }

    void setId(String id) {
        this.id = id;
    }

    String getName() {
        return name;
    }

    void setName(String name) {
        this.name = name;
    }
}